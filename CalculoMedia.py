# Calculo da média de duas notas
nota1 = float(input("Digite a primeira nota do aluno "))
nota2 = float(input("Digite a sugunda nota do aluno "))
media = (nota1 + nota2) / 2

"""
Este bloco de código 
verifica se a média 
é maior ou igual a 70.
"""
if media >= 70:
    print("Aprovado")
else:
    print("Reprovado")    