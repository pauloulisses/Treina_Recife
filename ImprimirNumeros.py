# FOR= percorre uma lista de tarefas e executa uma açãoespecífica para cada intem da lista
# I= variável
#IN= é um operador usado para verificar se um valor está presente em uma sequência (como: Lista, tuplas, strings ou conjunto)
# RANGER= é uma função embutida que gera uma sequência de números inteiros

for i in range(1, 101):
    print(i)


# WHILE = "ENQUANTO"
    
    i = 1 

    while i <= 100: # E nquanto a variável i for menor ou igual a 100 encremente 1
        print(i)
        i += 1