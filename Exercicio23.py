# Entrada de dados pelo usuário
x = int(input("Digite o valor de x: "))
y = int(input("Digite o valor de y: "))


# Exibindo as comparações com operadores relacionais
print(x > y) # x é maior que y
print(x == y) # x é igual a y
print(x < y) # x  é menor que y
print(x != y) # x é diferente de y
print(x >= y) # x é maior ou igual a y
print(x <= y) # x é menor ou igual a y


# Exibindo as comparações com operadores relacionais saídas serão 1 e 0. o int irá fazer isso para que saídas saiam 1 e 0
print(int(x > y)) # x é maior que y
print(int(x == y)) # x é igual a y
print(int(x < y)) # x  é menor que y
print(int(x != y)) # x é diferente de y
print(int(x >= y)) # x é maior ou igual a y
print(int(x <= y)) # x é menor ou igual a y