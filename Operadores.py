# Operadores

a = 10
b = 5

soma = a + b
subtracao = a - b
multiplicacao = a * b
divisao = a / b
divisao_inteira = a // b
resto_divisao = a % b
exponenciacao = a ** b

print("soma:", soma)
print("subtracao:", subtracao)
print("multiplicacao:", multiplicacao)
print("divisao:", divisao)
print("divisao_inteira:", divisao_inteira)
print("resto_divisao:", resto_divisao)
print("exponenciacao:", exponenciacao)